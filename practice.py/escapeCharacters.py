# \n : 문장 내 줄바꿈

print("백문이 불여일견\n백견이 불여일타")

# 저는 "조경은"입나다.
print("저는 '조경은'입니다.")
print('저는 "조경은"입니다.')
print("저는 \"조경은\"입니다.")
print("저는 \'조경은\'입니다.")


# \\ : 문장 내 \
print("i don't like apple\\.") # i don't like apple\.

#\r : 커서를 맨 앞 
print("red apple\rPine") # Pineapple

#\b : backspace 한 글자 삭제
print("redd\b apple") # red apple

#\t : tab
print("Red\tApple") #Red    Apple